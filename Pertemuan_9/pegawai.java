package Pertemuan_9;

public class pegawai {

    protected String nama;
    protected double gaji;

    public pegawai() {
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNama() {
        return nama;
    }

    public void setGaji(double gaji) {
        this.gaji = gaji;
    }

    public double getGaji() {
        return gaji;
    }
}
